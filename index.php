<h3 id="Actions-logActivity">Log Activity</h3>
<h4>Title</h4>

<p>logActivity</p>
<h4>Body</h4>

<p>Enter the following data in the following order for the Body field of the Create Wordpress Post Action in an IFTTT
	recipe:
	<code>{{date}},{{activityId}},{{duration}},{{calories}},{{distance}},{distanceUnit}},{{activityName}}</code></p>

<p>
<table class="table table-bordered table-hover">
	<tbody>
	<tr>
		<th>Field</th>
		<th>Required/Optional Status</th>
		<th>Info</th>
	</tr>
	<tr>
		<td><p>activityId</p></td>
		<td><p>optional/required</p></td>
		<td><p><em>Activity id</em>; id of the activity, directory activity or intensity level activity; if you pass
				directory activity id Fitbit will calculate and substitute it with the intensity level activity id,
				based on distance/duration</p></td>
	</tr>
	<tr>
		<td><p>activityName</p></td>
		<td><p>optional/required</p></td>
		<td><p><em>Custom activity name</em>; either <em>activityId</em> or <em>activityName</em> should be provided</p>
		</td>
	</tr>
	<tr>
		<td><p>manualCalories</p></td>
		<td><p>optional/required</p></td>
		<td><p><em>Manual calories</em>; amount of calories defined manually; required with <em>activityName</em> parameter,
				otherwise optional</p></td>
	</tr>
	<tr>
		<td><p>duration</p></td>
		<td><p>required</p></td>
		<td><p><em>Duration</em>; in milliseconds</p></td>
	</tr>
	<tr>
		<td><p>date</p></td>
		<td><p>optional</p></td>
		<td><p><em>Log entry date</em>; to match the format August 23, 2012 at 11:01PM. If left blank current time is
				used.</p></td>
	</tr>
	<tr>
		<td><p>distance</p></td>
		<td><p>optional/required</p></td>
		<td><p><em>Distance</em>; required for logging directory activity; in the format X.XX, in the selected <em>distanceUnit</em> or
				in the <a href="https://wiki.fitbit.com/display/API/API+Unit+System">unit system</a> that corresponds to
				the Accept-Language header provided</p></td>
	</tr>
	<tr>
		<td><p>distanceUnit</p></td>
		<td><p>optional</p></td>
		<td><p><a href="https://wiki.fitbit.com/display/API/API-Distance-Unit"><em>Distance measurement
						unit</em></a>; <em>Steps</em> units are available only for "Walking" (activityId=90013) and
				"Running" (activityId=90009) directory activities and their intensity levels</p></td>
	</tr>
	</tbody>
</table>
<small> Table modified from <a href="https://wiki.fitbit.com/display/API/API-Log-Activity">Fitbit API </a></small>
</p>
